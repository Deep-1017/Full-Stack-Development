let a = 2 + 4;

switch (a) {
  case 1:
    console.log("Too Small");
    break;
  case 2:
    console.log("Small");
    break;
  case 3:
    console.log("Almost");
    break;
  case 4:
    console.log("Exact");
    break;
  case 5:
    console.log("Bigger");
    break;
  default:
    console.log("Not Valid");
    break;
}
